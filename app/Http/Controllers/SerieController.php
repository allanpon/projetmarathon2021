     */
    public function index(Request $request)
    {
        $search=$request->get("recherche");
        if(empty($search)){
            $series = Serie::all();
        }
        else{
            $series = Serie::where("nom","like", "%". $request->get("recherche")."%")->get();
        }

        return view('listeSeries', ['data' => $series,"search" => $search]);
    }

